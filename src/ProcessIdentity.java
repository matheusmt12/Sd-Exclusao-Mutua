import java.util.ArrayList;

public class ProcessIdentity {

	private int temp;
	private int port;
	

	
	public ProcessIdentity( int temp,int port) {
		this.temp = temp;
		this.port = port;
	}

	public int getTemp() {
		return temp;
	}

	public void setTemp(int temp) {
		this.temp = temp;
	}

	public int getPort() {
		return port;
	}

	public void setPort(int port) {
		this.port = port;
	}
	
	
	
}
